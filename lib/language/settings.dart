settingsTitle(int language) {
  switch (language) {
    case 0:
      return "Settings";
    case 1:
      return "Configuraciones";
  }
}

settingsLanguageSelector(int language) {
  switch (language) {
    case 0:
      return "Language";
    case 1:
      return "Idioma";
  }
}

settingsThemeSelector(int language) {
  switch (language) {
    case 0:
      return "Application theme";
    case 1:
      return "Tema";
  }
}
